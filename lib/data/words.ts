export function getRandomWord(): string {
  console.log("[WORD LIST] Getting random word");
  const word = WORD_LIST[Math.floor(Math.random() * WORD_LIST.length)];
  console.log("[WORD LIST] Selected word:", word);
  return word;
}

export function isValidWord(word: string): boolean {
  const upperWord = word.toUpperCase();
  const isValid = WORD_LIST.includes(upperWord);
  console.log("[WORD LIST] Validating word:", upperWord, "- Valid:", isValid);
  return isValid;
}

export function generateRoomCode(): string {
  const chars = "ABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789";
  let code = "";
  for (let i = 0; i < 6; i++) {
    code += chars.charAt(Math.floor(Math.random() * chars.length));
  }
  console.log("[WORD LIST] Generated room code:", code);
  return code;
}
